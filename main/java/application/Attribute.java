package main.java.application;

public class Attribute {
	public String name = "";
	public String type = "";
	public String value = "";
	public String comment = "";
	
	public Attribute(String name, String type, String value, String comment){
		this.name = name;
		this.type = type;
		this.value = value;
		this.comment = comment;
	}
}
